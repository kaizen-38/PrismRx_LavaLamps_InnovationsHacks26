// ── Helper: build coverage matrix from MOCK_POLICIES ─────────────────────────

export function buildMockMatrix(): CoverageMatrixData {
  const payer_ids = PAYER_IDS
  const payer_labels = PAYERS

  const rows: MatrixRow[] = DRUG_FAMILIES.map((drug) => {
    const cells: Record<string, MatrixCell> = {}
    for (const payer_id of payer_ids) {
      const policy = MOCK_POLICIES.find(
        (p) => p.canonical_drug_key === drug.key && p.payer_id === payer_id
      )
      cells[payer_id] = policy
        ? {
            policy_id:       policy.id,
            coverage_status: policy.coverage_status,
            friction_score:  policy.friction_score,
            pa_required:     policy.pa_required,
            effective_date:  policy.effective_date,
            version_label:   policy.version_label,
          }
        : {
            policy_id:       null,
            coverage_status: 'unclear',
            friction_score:  0,
            pa_required:     false,
            effective_date:  '',
            version_label:   '',
          }
    }
    return {
      drug_key:          drug.key,
      drug_display_name: drug.display_name,
      drug_short_name:   drug.short_name,
      reference_product: drug.reference_product,
      biosimilars:       drug.biosimilars,
      cells,
    }
  })

  return {
    payer_ids,
    payer_labels,
    rows,
    generated_at: new Date().toISOString(),
  }
}

export function getPolicyById(id: string): PolicyDNA | undefined {
  return MOCK_POLICIES.find((p) => p.id === id)
}

